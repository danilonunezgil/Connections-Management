CREATE TABLE FACULTAD
(
  ID_FACULTAD  INTEGER,
  NOMBRE       VARCHAR(50)
);

CREATE TABLE PROGRAMA
(
  FACULTAD     INTEGER,
  ID_PROGRAMA  INTEGER,
  NOMBRE       VARCHAR(50)
);

CREATE TABLE MATERIAS
(
  FACULTAD    INTEGER,
  PROGRAMA    INTEGER,
  ID_MATERIA  INTEGER,
  NOMBRE      VARCHAR(50)
);

CREATE TABLE ESTUDIANTE
(
  CODIGO        INTEGER,
  NOMBRES       VARCHAR(50),
  APELLIDO1     VARCHAR(50),
  APELLIDO2     VARCHAR(50),
  TELEFONO      VARCHAR(50),
  FACULTAD      INTEGER,
  PROGRAMA      INTEGER,
  FECHA_INICIO  DATE
);

CREATE TABLE INSCRIPCIONES
(
  ESTUDIANTE  INTEGER,
  ANO         INTEGER,
  PERIODO     INTEGER,
  FECHA       DATE                              DEFAULT CURRENT_DATE,
  FACULTAD    INTEGER,
  PROGRAMA    INTEGER,
  MATERIA     INTEGER,
  NOTA        NUMERIC(4,2)
);


